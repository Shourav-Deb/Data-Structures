    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jComboBox1.getSelectedItem().toString().equals("...") || jTextField4.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Fill All the Fields !", "Blank Field", JOptionPane.ERROR_MESSAGE);

            } else if (!(Pattern.matches("^\\d{4}-\\d{1}-\\d{2}-\\d{3}$", jTextField1.getText()))) {
                JOptionPane.showMessageDialog(this, "                   Pattern Mismatch ! \n ( Year-Semister No.-Department No.-Roll )  \n                 (ex. 2023-1-30-111)", "Incorrect Pattern ", JOptionPane.ERROR_MESSAGE);

            } else if (!(Pattern.matches("^[0-3](\\.[0-9][0-9])?|[0-3](\\.[0-9])?|4(\\.00)?$", jTextField4.getText()))) {
                JOptionPane.showMessageDialog(this, "Incorrect CGPA !", "Error", JOptionPane.ERROR_MESSAGE);

            } else {

                if (search(jTextField1.getText()) == null) {
                    addStudent();
                    JOptionPane.showMessageDialog(this, "Student Data Entered !");
                    jButton4ActionPerformed(evt);
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jTextField4.setText("");

                } else {
                    JOptionPane.showMessageDialog(this, "Student Already Exists !");
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Incorrect Entry !", "Error Field", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

