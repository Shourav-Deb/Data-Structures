    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        if (jTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Field need to be filled !", "Blank Fields", JOptionPane.ERROR_MESSAGE);
        } else if (!(Pattern.matches("^\\d{4}-\\d{1}-\\d{2}-\\d{3}$", jTextField5.getText()))) {
            JOptionPane.showMessageDialog(this, "Recheck your ID !", "Incorrect Pattern ! ", JOptionPane.ERROR_MESSAGE);

        } else {
            Node current = search(jTextField5.getText());
            if (current != null) {
                JOptionPane.showMessageDialog(this, "Student Data Found !");
                jButton3.setEnabled(true);
                model.setNumRows(0);
                String data[] = {current.id, current.name, current.course, Double.toString(current.cgpa)};
                model.addRow(data);

            } else {
                JOptionPane.showMessageDialog(this, "Student Data Not Found !");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

