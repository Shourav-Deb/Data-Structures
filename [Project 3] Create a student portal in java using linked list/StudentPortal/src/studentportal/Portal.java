    public void addStudent() {
        
        Node newNode = new Node(jTextField1.getText(), jTextField2.getText(), jComboBox1.getSelectedItem().toString(), Double.parseDouble(jTextField4.getText()));

        // If the linked list is empty or the roll number of the new node is less than or equal to the roll number of the first node
        if (start == null || compareIDs(newNode.id, start.id) <= 0) {
            newNode.next = start;
            start = newNode;
        } else {
            Node current = start;
            // If the roll number of the new node is greater than the roll number of the first node
            while (current.next != null && compareIDs(newNode.id, current.next.id) > 0) {
                current = current.next;
            }
            // Insert the new node after the current node (current.next)
            newNode.next = current.next;
            current.next = newNode;
        }
    }

    public int compareIDs(String id1, String id2) {

        // Split the roll numbers into parts
        String[] parts1 = id1.split("-");
        String[] parts2 = id2.split("-");

        // Compare each part numerically
        for (int i = 0; i < 4; i++) {
            int num1 = Integer.parseInt(parts1[i]);
            int num2 = Integer.parseInt(parts2[i]);

            if (num1 < num2) {
                return -1;
            } else if (num1 > num2) {
                return 1;
            }
        }
        return 0;    // Roll numbers are equal
    }

    public Node search(String id) {
        Node current = start;
        while (current != null) {
            if (current.id.equals(id)) {
                return current; // Found the student with the given ID
            }
            current = current.next;
        }
        return null; // Student with the given ID not found
    }

    public void delete(String id) {
        Node current = start;
        Node previous = null;

        while (current != null) {
            if (current.id.equals(id)) {
                if (previous == null) {
                    start = current.next;   // If the student to delete is the first node
                } else {
                    previous.next = current.next;   // If the student to delete is not the first node
                }
                System.out.println("Student" + id + " has been deleted.");
                return;
            }

            previous = current;
            current = current.next;
        }
    }

